    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         int ri= table.getSelectedRow();// gives index of selected row
        if(ri!=-1)// ri==-1 if no row selected
        {    
        int r=JOptionPane.showConfirmDialog(null,"Return selected Book?","Confirm",JOptionPane.YES_NO_OPTION);
         if(r==JOptionPane.YES_OPTION)
        {
        String id= (String)table.getValueAt(ri, 0);//downcasting object to String
        String date= (String)table.getValueAt(ri, 5);
        String sid=  (String)table.getValueAt(ri, 4);
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");  
         Date today = new Date(); 
         long days_difference=0;
         try
         {
         Date due=formatter.parse(date); 
         long time_difference= today.getTime() - due.getTime();     
         days_difference = (time_difference / (1000*60*60*24)) % 365;  
         }
          catch (ParseException excep) {   
            excep.printStackTrace();   
        }   
         
        try
        {
            long fine=0;
            if(days_difference>0)
            {
                 fine=15*days_difference;
                 JOptionPane.showMessageDialog(null,"A fine of Rs"+fine+"to be paid for late payment");
            }
            String q5="UPDATE books SET ISSUE_STATUS = 0, Student_Id= NULL, Due_Date=NULL WHERE Book_Id = '"+id+"'";
            Database.DbConnect.s.executeUpdate(q5);
            JOptionPane.showMessageDialog(null,"Book Returned successfully");
            int noofbooks=0;
            String q2="SELECT NoOfBooks FROM user WHERE UserId='"+sid+"'";
            ResultSet rs=Database.DbConnect.s.executeQuery(q2);
            if(rs.next())
               noofbooks=rs.getInt("NoOfBooks");
            String q3="UPDATE user SET NoOfBooks = noofbooks-1, Fine=fine WHERE UserId = '"+sid+"'";
            Database.DbConnect.s.executeUpdate(q3);
            
            getEntries();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        }
        }
        
        else
         {
             JOptionPane.showMessageDialog(null,"No book selected");
         }
    }//GEN-LAST:event_jButton1ActionPerformed

