    private void getEntries()
    {
        try
        {
            javax.swing.table.DefaultTableModel dtm= 
                        (javax.swing.table.DefaultTableModel)table.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0)
            {
                dtm.removeRow(0);
            }
            
            ResultSet rs=Database.DbConnect.s.executeQuery("select * from books "
                    + " order by Name asc");
            int total=0;//serial number
            //fetch data of all rows from database
            while(rs.next()) // get data of rows one by one
            {
                String id=rs.getString(1);
                String name=rs.getString(2);
                String author=rs.getString(3);
                boolean status=rs.getBoolean(4);
                String sid=rs.getString(5);
                String dueDate= rs.getString(6);
                
                 Object o[]={id,name,author,status,sid,dueDate};
                 dtm.addRow(o);
            }
        }
        catch(Exception e)
        {
           JOptionPane.showMessageDialog(null,e);     
        }
    }

